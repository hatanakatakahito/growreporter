/**
 * ページタイトルとメタディスクリプションを設定するユーティリティ関数
 * @param {string} pageName - ページ名（例: "ダッシュボード"）
 * @param {string} [description] - カスタムディスクリプション（省略時はPAGE_DESCRIPTIONSから自動取得）
 */
export const setPageTitle = (pageName, description) => {
  if (pageName) {
    document.title = `グローレポータ｜${pageName}`;
  } else {
    document.title = 'グローレポータ';
  }

  const desc = description || PAGE_DESCRIPTIONS[pageName] || DEFAULT_DESCRIPTION;
  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', desc);
  }
};

/**
 * ページのメタディスクリプションを設定
 * @param {string} description - メタディスクリプション
 */
export const setMetaDescription = (description) => {
  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', description);
  }
};
